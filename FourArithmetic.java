public class FourArithmetic {
    public static void main(String[] args){
        // int a = 10;
        // int b = 5;
        // System.out.println(a+b);
        // System.out.println(a-b);
        // System.out.println(a*b);
        // System.out.println(a/b);
        int i = 0;
        int j = 10;
        i++;
        //i = i + 1
        j++;

        System.out.println(i++); //i를 찍고 1더한후
        System.out.println(i); // 1더한값이 찍혀라

        System.out.println(++j); //먼저 1을 더한거 찍고
        System.out.println(j); //j찌겅라

        //"3".equals("2") 문자가 같을때는 equal사용
    }
}
